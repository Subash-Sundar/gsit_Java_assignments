package gsitAssignments;

/*Question

Write a Java program to store the following details using appropriate data types and display them:

Student ID = 101
Grade = A
Passed = true
Age = 18

Print all values on the console.*/

public class AssignmentOne_220626 {
    public static void main(String[] args) {
        int studentId = 101;
        char grade = 'A';
        boolean passed = true;
        byte age = 18;

        System.out.println("Student ID = "+studentId);
        System.out.println("Grade = "+grade);
        System.out.println("Passed = "+passed);
        System.out.println("Age = "+age);
    }
}
